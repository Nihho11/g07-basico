"""Volumen de una esfera"""

#formula del volumen de una esfera: V=4/3*pi*r**3

#declarar variables

pi=3.14

#radio puede ser cualquiera, definiremos un valor 10

r=10

Volumen=(4/3)*pi*(r**3)

#mostrar datos

print('El volumen de una esfera de radio {} considerando pi igual a {} es: {} unidades c√∫bicas'.format(r,pi,Volumen))